package inheritance_example;

public class Doctor {
	
	String name;
	String licenceno;
	String clinicname;
	
	Doctor()
	{
		
	}
	public Doctor(String name, String licenceno, String clinicname) {
		super();
		this.name = name;
		this.licenceno = licenceno;
		this.clinicname = clinicname;
	}
	
	
	

}
